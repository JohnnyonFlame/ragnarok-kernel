void ams495_reset(struct ams495 *ctx)
{
	gpiod_set_value_cansleep(ctx->enable_gpio, 1);
	msleep(2);
	gpiod_set_value_cansleep(ctx->enable_gpio, 0);
	msleep(2);
}

static void ams495_init_sequence(struct ams495 *ctx)
{
	ams495_dcs_write_seq_static(ctx, 0xF0, 0x5A, 0x5A); // MCS Access password 1
	
	// gtcon set
	ams495_dcs_write_seq_static(ctx, 0xF7, 0x20);

	// gateless signal
	ams495_dcs_write_seq_static(ctx, 0xF8,	0xF7, 0x7A, 0x89, 0x67, 0x26,
						0x38, 0x00, 0x00, 0x09, 0x67,
						0x70, 0x88, 0x7a, 0x76, 0x05,
						0x09, 0x23, 0x23, 0x23, 0x00);

	// set power control
	ams495_dcs_write_seq_static(ctx, 0xF4, 0x33, 0x42, 0x00, 0x08);
	ams495_dcs_write_seq_static(ctx, 0xF5, 0x00, 0x06, 0x26, 0x35, 0x03);

	// set gamma
	ams495_dcs_write_seq_static(ctx, 0xF9,	0x01, 0x9F, 0xF9, 0xBE, 0xCF,
						0xD7, 0xC9, 0xC2, 0xCB, 0xBB,
						0xE1, 0xE3, 0xDE, 0xD6, 0xD0,
						0xD3, 0xFA, 0xED, 0xE6, 0x2F,
						0x00, 0x2F);

	// set elvss
	ams495_dcs_write_seq_static(ctx, 0xB1, 0x07, 0x00, 0x00);
	ams495_dcs_write_seq_static(ctx, 0xB2, 0x12, 0x12, 0x12);


	// exit sleep mode
	msleep(50);
	ams495_dcs_write_seq_static(ctx, 0x11);

	// set display on
	msleep(300);
	ams495_dcs_write_seq_static(ctx, 0x29);
}

static int ams495_unprepare(struct drm_panel *panel)
{
	struct ams495 *ctx = panel_to_ams495(panel);

	msleep(120);
	ams495_dcs_write_seq_static(ctx, MIPI_DCS_SET_DISPLAY_OFF);
	ams495_dcs_write_seq_static(ctx, MIPI_DCS_ENTER_SLEEP_MODE);
	msleep(40);

	ams495_clear_error(ctx);

	return 0;
//	return ams495_power_off(ctx);
}

static int ams495_prepare(struct drm_panel *panel)
{
	struct ams495 *ctx = panel_to_ams495(panel);
	int ret = 0;

	// enable regulators
	
	// enable gpio
	gpiod_set_value_cansleep(ctx->enable_gpio, 1);
	msleep(60);
	ams495_reset(ctx);
	msleep(20);
	ams495_init_sequence(ctx);

	ret = ams495_clear_error(ctx);

	if (ret < 0)
		ams495_unprepare(panel);

	ctx->prepared = true;
	return ret;
}

static const struct drm_display_mode samsung_ams495_mode = {
	.clock		= ((960 + 10 + 2 + 10) * (544 + 10 + 2 + 10) * 60) / 1000,
	.hdisplay	= 960,
	.hsync_start	= 960 + 10,
	.hsync_end	= 960 + 10 + 2,
	.htotal		= 960 + 10 + 2 + 10,
	.vdisplay	= 544,
	.vsync_start	= 544 + 10,
	.vsync_end	= 544 + 10 + 2,
	.vtotal		= 544 + 10 + 2 + 10,
	.width_mm	= 70,
	.height_mm	= 117,
};


static int ams495_get_modes(struct drm_panel *panel,
			    struct drm_connector *connector)
{
	struct ams495 *ctx = panel_to_ams495(panel);
	struct drm_display_mode *mode;

	mode = drm_mode_duplicate(connector->dev, &samsung_ams495_mode);
	if (!mode)
		return -ENOMEM;

	drm_mode_set_name(mode);

	mode->type = DRM_MODE_TYPE_DRIVER | DRM_MODE_TYPE_PREFERRED;
	connector->display_info.width_mm = mode->width_mm;
	connector->display_info.height_mm = mode->height_mm;
	drm_mode_probed_add(connector, mode);

	return 1;
}

static const struct drm_panel_funcs ams495_drm_funcs = {
	.prepare = ams495_prepare,
	.unprepare = ams495_unprepare,
	.get_modes = ams495_get_modes,
};

static int ams495_probe(struct spi_device *spi)
{
	struct device *dev = &spi->dev;
	struct ams495 *ctx;
	struct device_node *endpoint, *dsi_host_node;
	struct mipi_dsi_host *host;
	int ret;
	struct mipi_dsi_device_info info = {
		.type = "dupa",
		.channel = 0,
		.node = NULL,
	};

	ctx = devm_kzalloc(dev, sizeof(struct ams495), GFP_KERNEL);
	if (!ctx)
		return -ENOMEM;

	spi_set_drvdata(spi, ctx);

	ctx->dev = dev;
	//ctx->brightness = ARRAY_SIZE(ams495_gammas) - 1;

	ctx->reset_gpio = devm_gpiod_get(dev, "reset", GPIOD_OUT_HIGH);
	if (IS_ERR(ctx->reset_gpio)) {
		dev_err(dev, "cannot get reset-gpios %ld\n",
			PTR_ERR(ctx->reset_gpio));
		return PTR_ERR(ctx->reset_gpio);
	}

	spi->bits_per_word = 8;
	ret = spi_setup(spi);
	if (ret < 0) {
		dev_err(dev, "spi setup failed.\n");
		return ret;
	}


	endpoint = of_graph_get_next_endpoint(dev->of_node, NULL);
	if (!endpoint) {
		dev_err(dev, "failed to get endpoint\n");
		return -ENODEV;
	}

	dsi_host_node = of_graph_get_remote_port_parent(endpoint);
	if (!dsi_host_node)
	{
		dev_err(dev, "failed to get remote port parent\n");
		goto put_endpoint;
	}

	host = of_find_mipi_dsi_host_by_node(dsi_host_node);
	if (!host) {
		dev_err(dev, "failed to get dsi host node\n");
		ret = -EPROBE_DEFER;
		goto put_host;
	}

	info.node = of_graph_get_remote_port(endpoint);
	if (!info.node) {
		dev_err(dev, "failed to get remote port node\n");
		ret = -ENODEV;
		goto put_host;
	}

	ctx->dsi = mipi_dsi_device_register_full(host, &info);
	if (IS_ERR(ctx->dsi)) {
		dev_err(dev, "failed to register dsi device: %ld\n",
			PTR_ERR(ctx->dsi));
		ret = PTR_ERR(ctx->dsi);
		goto put_host;
	}

	drm_panel_init(&ctx->panel, dev, &ams495_drm_funcs,
		       DRM_MODE_CONNECTOR_DSI);

	drm_panel_add(&ctx->panel);

	of_node_put(dsi_host_node);
	of_node_put(endpoint);
	return 0;

put_host:
	of_node_put(dsi_host_node);

put_endpoint:
	of_node_put(endpoint);
	return -ENODEV;
}

static void ams495_remove(struct spi_device *spi)
{
	struct ams495 *ctx = spi_get_drvdata(spi);

	//ams495_power_off(ctx);
	drm_panel_remove(&ctx->panel);
}

